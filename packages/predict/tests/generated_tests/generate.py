# ====================================================================
# Oracle ground truth computation
# ====================================================================


def svi_total_variance(
    k: float, a: float, b: float, rho: float, m: float, sigma: float
) -> float:
    """SVI total variance: a + b * (rho*(k-m) + sqrt((k-m)^2 + sigma^2))"""
    km = k - m
    return a + b * (rho * km + math.sqrt(km * km + sigma * sigma))


def binary_price(
    forward: float,
    strike: float,
    a: float,
    b: float,
    rho: float,
    m: float,
    sigma: float,
    rate: float,
    t: float,
    is_call: bool,
) -> float:
    """Binary option price via SVI + Black-Scholes.

    Binary call (UP) = discount * N(d2)
    Binary put (DN)  = discount * N(-d2)
    d2 = (-k - total_var/2) / sqrt(total_var), k = ln(strike/forward)
    """
    k = math.log(strike / forward)
    tv = svi_total_variance(k, a, b, rho, m, sigma)
    if tv <= 0:
        raise ValueError(f"Non-positive total variance: {tv}")
    sqrt_tv = math.sqrt(tv)
    d2 = (-k - tv / 2) / sqrt_tv
    discount = math.exp(-rate * t)
    return discount * norm.cdf(d2 if is_call else -d2)

def price_legal_strikes(
    forward: float, svi: dict, rate: float, t: float, strikes: list[int],
) -> list[dict]:
    priced = []
    for strike in strikes:
        strike_float = strike / FLOAT_SCALING
        up = binary_price(forward, strike_float, **svi, rate=rate, t=t, is_call=True)
        dn = binary_price(forward, strike_float, **svi, rate=rate, t=t, is_call=False)
        priced.append({
            "strike": strike,
            "expected_up": to_float_scaled(up),
            "expected_dn": to_float_scaled(dn),
        })

    return priced


def mint_spread(price: float) -> float:
    """Bernoulli spread with a floor at the configured minimum."""
    variance = price * (1.0 - price)
    return max(DEFAULT_BASE_SPREAD * math.sqrt(variance), DEFAULT_MIN_SPREAD)


def utilization_spread(liability: float, balance: float) -> float:
    """Current predict quote model penalizes utilization quadratically."""
    if liability <= 0.0 or balance <= 0.0:
        return 0.0
    util = min(liability / balance, 1.0)
    return DEFAULT_BASE_SPREAD * DEFAULT_UTIL_MULTIPLIER * util * util


def svi_to_move(svi: dict, rate: float) -> dict:
    """Convert signed-float SVI params to Move-format unsigned + sign flag.
    Uses round() to avoid floating-point truncation artifacts."""
    def to_scaled(v: float) -> int:
        return round(abs(v) * FLOAT_SCALING)
    return {
        "a": to_scaled(svi["a"]),
        "b": to_scaled(svi["b"]),
        "rho": to_scaled(svi["rho"]),
        "rho_neg": svi["rho"] < 0,
        "m": to_scaled(svi["m"]),
        "m_neg": svi["m"] < 0,
        "sigma": to_scaled(svi["sigma"]),
        "rate": to_scaled(rate),
    }


