ORACLE_FIELDS = [
    ("spot", "u64"), ("forward", "u64"),
    ("a", "u64"), ("b", "u64"), ("rho", "u64"), ("rho_neg", "bool"),
    ("m", "u64"), ("m_neg", "bool"), ("sigma", "u64"), ("rate", "u64"),
    ("expiry_ms", "u64"), ("now_ms", "u64"),
    ("min_strike", "u64"), ("max_strike", "u64"), ("tick_size", "u64"),
]

SCENARIO_FIELDS = [name for name, _ in ORACLE_FIELDS]


def emit_oracle_structs(w: MoveWriter):
    w.blank()
    w.raw("public struct StrikePoint has copy, drop {")
    w.raw("    strike: u64,")
    w.raw("    expected_up: u64,")
    w.raw("    expected_dn: u64,")
    w.raw("}")
    w.blank()
    w.raw("public struct OracleScenario has copy, drop {")
    for name, typ in ORACLE_FIELDS:
        w.raw(f"    {name}: {typ},")
    w.raw("    strike_points: vector<StrikePoint>,")
    w.raw("}")


def emit_oracle_getters(w: MoveWriter):
    w.blank()
    for name, typ in ORACLE_FIELDS:
        w.raw(f"public fun {name}(s: &OracleScenario): {typ} {{ s.{name} }}")
    w.raw(
        "public fun strike_points(s: &OracleScenario): "
        "&vector<StrikePoint> { &s.strike_points }"
    )
    w.blank()
    w.raw("public fun strike(sp: &StrikePoint): u64 { sp.strike }")
    w.raw("public fun expected_up(sp: &StrikePoint): u64 { sp.expected_up }")
    w.raw("public fun expected_dn(sp: &StrikePoint): u64 { sp.expected_dn }")


def emit_oracle_scenario(w: MoveWriter, scenario: dict, label: str, index: int):
    w.raw(f"        // Index {index}: {label}")
    w.raw("        OracleScenario {")
    for field in SCENARIO_FIELDS:
        val = scenario[field]
        formatted = to_bool(val) if isinstance(val, bool) else fmt_u64(val)
        w.raw(f"            {field}: {formatted},")
    w.raw("            strike_points: vector[")
    for sp in scenario["strike_points"]:
        w.raw(
            f"                StrikePoint {{ "
            f"strike: {fmt_u64(sp['strike'])}, "
            f"expected_up: {fmt_u64(sp['expected_up'])}, "
            f"expected_dn: {fmt_u64(sp['expected_dn'])} }},"
        )
    w.raw("            ],")
    w.raw("        },")


def emit_scenarios_vector(
    w: MoveWriter, scenarios: list[dict],
):
    w.blank()
    w.raw("public fun scenarios(): vector<OracleScenario> {")
    w.raw("    vector[")
    for i, scenario in enumerate(scenarios):
        emit_oracle_scenario(w, scenario, scenario["name"], i)
    w.raw("    ]")
    w.raw("}")


def generate_oracle():
    """Generate generated_oracle.move."""
    all_scenarios = build_priced_scenarios_from_legal_inputs(ORACLE_SCENARIOS)

    print(
        f"Oracle: {len(ORACLE_SCENARIOS)} frozen legal scenarios"
    )

    w = MoveWriter()
    w.header("generated_oracle")
    emit_oracle_structs(w)
    emit_oracle_getters(w)
    emit_scenarios_vector(w, all_scenarios)
    w.write(ORACLE_OUTPUT)


