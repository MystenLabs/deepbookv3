def to_float_scaled(value: float) -> int:
    """Convert to FLOAT_SCALING (1e9), floor-truncated via int().
    Note: svi_to_move() uses round() instead to avoid float representation
    artifacts (e.g. 0.00416 * 1e9 = 4159999.999...)."""
    return int(value * FLOAT_SCALING)


def to_bool(val) -> str:
    """Convert a Python truthy value to a Move bool literal."""
    return "true" if val else "false"


def fmt_u64(value: int) -> str:
    """Format an integer with underscore separators for Move readability."""
    return f"{value:_}"


def read_move_macro_u64(name: str) -> int:
    """Read a u64-valued macro from constants.move so generator math stays in sync."""
    pattern = rf"public macro fun {re.escape(name)}\(\): u64 \{{ ([0-9_]+) \}}"
    match = re.search(pattern, CONSTANTS_MOVE.read_text())
    if not match:
        raise ValueError(f"Could not find macro {name} in {CONSTANTS_MOVE}")
    return int(match.group(1).replace("_", ""))


# ====================================================================
# Move file writer
# ====================================================================


class MoveWriter:
    """Builds a #[test_only] Move module with generated constants."""

    def __init__(self):
        self.lines: list[str] = []

    def header(self, module_name: str):
        self.lines.append("// DO NOT EDIT — generated by generate.py")
        self.lines.append(
            "// Source of truth: scipy.stats.norm + Python math"
        )
        self.lines.append(
            "// Regenerate: cd tests/generated_tests && python3 generate.py && bunx prettier-move -c *.move --write"
        )
        self.lines.append("")
        self.lines.append("#[test_only]")
        self.lines.append(f"module deepbook_predict::{module_name};")

    def blank(self):
        self.lines.append("")

    def section(self, title: str):
        self.blank()
        self.lines.append(f"// === {title} ===")

    def raw(self, text: str):
        self.lines.append(text)

    def write(self, path: Path):
        with open(path, "w") as f:
            f.write("\n".join(self.lines))
            f.write("\n")
        print(f"Wrote {path} ({len(self.lines)} lines)")


