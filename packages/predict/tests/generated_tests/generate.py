def compute_ln(input_scaled: int) -> tuple[int, bool]:
    x = input_scaled / FLOAT_SCALING
    val = math.log(x)
    return to_float_scaled(abs(val)), val < 0


def compute_exp(input_scaled: int, is_negative: bool) -> int:
    x = input_scaled / FLOAT_SCALING
    return to_float_scaled(math.exp(-x if is_negative else x))


def compute_cdf(input_scaled: int, is_negative: bool) -> int:
    x = input_scaled / FLOAT_SCALING
    if x > 8.0:
        return 0 if is_negative else FLOAT_SCALING
    return to_float_scaled(norm.cdf(-x if is_negative else x))


# ====================================================================
# Math random input generation
# ====================================================================


def random_ln_inputs(rng: random.Random, n: int, existing: list[int]) -> list[int]:
    cases = list(existing)
    while len(cases) < len(existing) + n:
        t = rng.random() ** 2
        exp = t * LN_EXP_MAX
        x = min(U64_MAX, max(FLOAT_SCALING, int(10 ** exp)))
        if x not in cases:
            cases.append(x)
    return cases[len(existing):]


def random_exp_inputs(rng: random.Random, n: int, existing: list[tuple[int, bool]]) -> list[tuple[int, bool]]:
    cases = list(existing)
    half = n // 2
    pos_added = 0
    neg_added = 0

    while pos_added + neg_added < n:
        t = rng.random() ** 2
        if pos_added < half:
            entry = (int(t * MAX_EXP_INPUT), False)
            if entry not in cases:
                cases.append(entry)
                pos_added += 1
        else:
            x = int(t * EXP_NEG_MAX)
            entry = (x, True)
            if entry not in cases and compute_exp(x, True) > 0:
                cases.append(entry)
                neg_added += 1

    return cases[len(existing):]


def random_cdf_inputs(rng: random.Random, n: int, existing: list[tuple[int, bool]]) -> list[tuple[int, bool]]:
    cases = list(existing)
    half = n // 2
    added = 0

    while added < n:
        t = rng.random() ** 2
        x = int(t * CDF_RAND_MAX)
        is_neg = added >= half
        entry = (x, is_neg)
        if entry in cases:
            continue
        expected = compute_cdf(x, is_neg)
        if expected < 1_000 or expected > FLOAT_SCALING - 1_000:
            continue
        cases.append(entry)
        added += 1

    return cases[len(existing):]


