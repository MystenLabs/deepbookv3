def oracle_scenario_by_name(name: str) -> tuple[int, dict]:
    for idx, scenario in enumerate(ORACLE_SCENARIOS):
        if scenario["name"] == name:
            return idx, scenario
    raise ValueError(f"Unknown oracle scenario name: {name}")


